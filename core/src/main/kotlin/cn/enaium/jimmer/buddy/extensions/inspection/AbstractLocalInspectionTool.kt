package cn.enaium.jimmer.buddy.extensions.inspection

import com.intellij.codeInspection.LocalInspectionTool
import com.intellij.codeInspection.ProblemsHolder
import com.intellij.psi.PsiElement
import com.intellij.psi.PsiElementVisitor

/**
 * @author Enaium
 */
abstract class AbstractLocalInspectionTool : LocalInspectionTool() {
    override fun buildVisitor(holder: ProblemsHolder, isOnTheFly: Boolean): PsiElementVisitor {
        return object : PsiElementVisitor() {
            override fun visitElement(element: PsiElement) {
                visit(element, holder, isOnTheFly)
            }
        }
    }

    abstract fun visit(element: PsiElement, holder: ProblemsHolder, isOnTheFly: Boolean)
}